    public void loadTable(ArrayList<ViewModelNhanVien> list) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblNhanVienForm.getModel();
        defaultTableModel.setRowCount(0);
        for (ViewModelNhanVien viewModelNhanVien : list) {
            defaultTableModel.addRow(new Object[]{
                viewModelNhanVien.getMa(), viewModelNhanVien.getHo(), viewModelNhanVien.getTenDem(), viewModelNhanVien.getTen(), viewModelNhanVien.getGioiTinh(), viewModelNhanVien.getNgaySinh(), viewModelNhanVien.getDiaChi(), viewModelNhanVien.getSoDienThoai(), viewModelNhanVien.getMatKhau(), viewModelNhanVien.getTenCuaHang(), viewModelNhanVien.getTenChucVu(), viewModelNhanVien.getTrangThai() == 0 ? "Làm" : "Nghỉ"
            });
        }
    }

    // load comboboxChucVu
    public void loadComBoBoxTenChucVu() {
        List<ViewModelChucVu> list = iChucVuService.getAll();
        for (ViewModelChucVu viewModelChucVu : list) {
            String tenChucVu = viewModelChucVu.getTen();
            cbbChucVu.addItem(tenChucVu);
        }
    }

    // loadComBoBoxByTenCuaHang
    public void loadComBoBoxTenCuaHang() {
        List<ViewModelCuaHang> list = iCuaHangService.getAll();
        for (ViewModelCuaHang viewModelCuaHang : list) {
            String tenCuaHang = viewModelCuaHang.getTen();
            cbbCuaHang.addItem(tenCuaHang);     
        }
    }

