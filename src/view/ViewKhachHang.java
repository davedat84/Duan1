    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        ViewModelKhachHang viewModelKhachHang = getData();
        if (viewModelKhachHang == null) {
            return;
        }
        JOptionPane.showMessageDialog(this, iKhachHangSerivce.insert(viewModelKhachHang));
        loadTable(iKhachHangSerivce.getAll());
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblKhachHangForm.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để xoá");
            return;
        }
        String ma = tblKhachHangForm.getValueAt(row, 0).toString();
        JOptionPane.showMessageDialog(this, iKhachHangSerivce.deleteByMa(ma));
        loadTable(iKhachHangSerivce.getAll());
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        ViewModelKhachHang viewModelKhachHang = getData();
        int row = tblKhachHangForm.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để update");
            return;
        } else {
            String ma = tblKhachHangForm.getValueAt(row, 0).toString();
            JOptionPane.showMessageDialog(this, iKhachHangSerivce.update(ma, viewModelKhachHang));
            loadTable(iKhachHangSerivce.getAll());
        }
    }//GEN-LAST:event_btnSuaActionPerformed

