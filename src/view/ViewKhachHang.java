    public void loadTable(ArrayList<ViewModelKhachHang> list) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblKhachHangForm.getModel();
        defaultTableModel.setRowCount(0);
        for (ViewModelKhachHang viewModelKhachHang : list) {
            defaultTableModel.addRow(new Object[]{
                viewModelKhachHang.getMa(), viewModelKhachHang.getTen(), viewModelKhachHang.getTenDem(), viewModelKhachHang.getHo(), viewModelKhachHang.getNgaySinh(), viewModelKhachHang.getSoDienThoai(), viewModelKhachHang.getDiaChi(), viewModelKhachHang.getThanhPho(), viewModelKhachHang.getQuocGia(), viewModelKhachHang.getMatKhau()
            });
        }
    }

    public ViewModelKhachHang getData() {
        ViewModelKhachHang viewModelKhachHang = new ViewModelKhachHang();
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String tenDem = txtTenDem.getText();
        String ho = txtHo.getText();
        String ngaySinh = txtNgaySinh.getText();
        String soDienThoai = txtSDT.getText();
        String diaChi = txtDiaChi.getText();
        String thanhPho = txtThanhPho.getText();
        String quocGia = txtQuocGia.getText();
        String matKhau = txtMatKhau.getText();
       
        // theem vao
        viewModelKhachHang.setMa(ma);
        viewModelKhachHang.setTen(ten);
        viewModelKhachHang.setTenDem(tenDem);
        viewModelKhachHang.setHo(ho);
         
        // check chuẩn định dạng 
        try {
            LocalDate localDate = LocalDate.parse(ngaySinh);
            viewModelKhachHang.setNgaySinh(java.sql.Date.valueOf(localDate));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Chưa chuẩn định dạng ngày sinh");
            return null;
        }
        viewModelKhachHang.setSoDienThoai(soDienThoai);
        viewModelKhachHang.setDiaChi(diaChi);
        viewModelKhachHang.setThanhPho(thanhPho);
        viewModelKhachHang.setQuocGia(quocGia);
        viewModelKhachHang.setMatKhau(matKhau);
        return viewModelKhachHang;
    }

