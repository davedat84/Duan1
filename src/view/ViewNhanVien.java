    public ViewModelNhanVien getData() {
        ViewModelNhanVien viewModelNhanVien = new ViewModelNhanVien();
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String tenDem = txtTenDem.getText();
        String ho = txtHo.getText();
        String gioiTinh = txtGioiTinh.getText();
        String ngaySinh = txtNgaySinh.getText();
        String diaChi = txtDiaChi.getText();
        String soDienThoai = txtSDT.getText();
        String matKhau = txtMatKhau.getText();

        // lấy ra tên 
        String tenCH = cbbCuaHang.getSelectedItem().toString();
        String tenCV = cbbChucVu.getSelectedItem().toString();


        viewModelNhanVien.setMa(ma);
        viewModelNhanVien.setTen(ten);
        viewModelNhanVien.setTenDem(tenDem);
        viewModelNhanVien.setHo(ho);
        viewModelNhanVien.setGioiTinh(gioiTinh);

        viewModelNhanVien.setDiaChi(diaChi);
        viewModelNhanVien.setSoDienThoai(soDienThoai);
        viewModelNhanVien.setMatKhau(matKhau);
        
        viewModelNhanVien.setTenCuaHang(tenCH);
        viewModelNhanVien.setTenChucVu(tenCV);
        // thêm ngày sinh vào 
        try {
            LocalDate localDate = LocalDate.parse(ngaySinh);
            viewModelNhanVien.setNgaySinh(java.sql.Date.valueOf(localDate));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "chua dung dinh dang ngày tạo");
            return null;
        }

        // theem rdo vao
        if (rdoLam.isSelected()) {
            viewModelNhanVien.setTrangThai(0);
        } else {
            viewModelNhanVien.setTrangThai(1);
        }
        return viewModelNhanVien;
    }

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        ViewModelNhanVien viewModelNhanVien = getData();
        JOptionPane.showMessageDialog(this, iNhanVienService.insert(viewModelNhanVien));
        loadTable(iNhanVienService.getAll());
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblNhanVienForm.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để xoá");
            return;
        }
        String ma = tblNhanVienForm.getValueAt(row, 0).toString();
        JOptionPane.showMessageDialog(this, iNhanVienService.delete(ma));
        loadTable(iNhanVienService.getAll());
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tblNhanVienForm.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để sửa");
            return;
        }
        ViewModelNhanVien viewModelNhanVien = getData();
        String ma = tblNhanVienForm.getValueAt(row, 0).toString();
        JOptionPane.showMessageDialog(this, iNhanVienService.update(ma, viewModelNhanVien));
        loadTable(iNhanVienService.getAll());
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblNhanVienFormMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienFormMouseClicked
        int row = tblNhanVienForm.getSelectedRow();
        String ma = tblNhanVienForm.getValueAt(row, 0).toString();
        String ho = tblNhanVienForm.getValueAt(row, 1).toString();
        String tenDem = tblNhanVienForm.getValueAt(row, 2).toString();
        String ten = tblNhanVienForm.getValueAt(row, 3).toString();
        String gioiTinh = tblNhanVienForm.getValueAt(row, 4).toString();
        String ngaySinh = tblNhanVienForm.getValueAt(row, 5).toString();
        String diaChi = tblNhanVienForm.getValueAt(row, 6).toString();
        String sdt = tblNhanVienForm.getValueAt(row, 7).toString();
        String matKhau = tblNhanVienForm.getValueAt(row, 8).toString();
        String cuaHang = tblNhanVienForm.getValueAt(row, 9).toString();
        String chucVu = tblNhanVienForm.getValueAt(row, 10).toString();
        String trangThai = tblNhanVienForm.getValueAt(row, 11).toString();
        if (trangThai.equals("Làm")) {
            rdoLam.setSelected(true);
        } else {
            rdoNghi.setSelected(true);
        }
        txtMa.setText(ma);
        txtHo.setText(ho);
        txtTenDem.setText(tenDem);
        txtTen.setText(ten);
        txtGioiTinh.setText(gioiTinh);
        txtNgaySinh.setText(ngaySinh);
        txtDiaChi.setText(diaChi);
        txtSDT.setText(sdt);
        txtMatKhau.setText(matKhau);
        cbbCuaHang.setSelectedItem(cuaHang);
        cbbChucVu.setSelectedItem(chucVu);

    }//GEN-LAST:event_tblNhanVienFormMouseClicked

