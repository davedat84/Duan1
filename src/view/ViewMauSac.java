    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        int row = tblMauSacForm.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng  để xoá");
            return;
        }
        
        String ma = tblMauSacForm.getValueAt(row, 0).toString();
        JOptionPane.showMessageDialog(this, iMauSacService.delete(ma));
        loadTable(iMauSacService.getAll());
    }//GEN-LAST:event_btnXoaActionPerformed

    public ViewModelMauSac getData() {
        ViewModelMauSac viewModelMauSac = new ViewModelMauSac();
        String ma = txtMa.getText();
        String ten = txtTen.getText();

        viewModelMauSac.setMa(ma);
        viewModelMauSac.setTen(ten);
        return viewModelMauSac;
    }

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        JOptionPane.showMessageDialog(this, iMauSacService.insert(getData()));
        loadTable(iMauSacService.getAll());
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tblMauSacForm.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để sủa");
            return;
        }
        String ma = tblMauSacForm.getValueAt(row, 0).toString();
        ViewModelMauSac viewModelMauSac = getData();
        JOptionPane.showMessageDialog(this, iMauSacService.update(ma, viewModelMauSac));
        loadTable(iMauSacService.getAll());
    }//GEN-LAST:event_btnSuaActionPerformed

