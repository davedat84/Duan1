    public void loadTable(List<SanPham> list) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblSanPhamForm.getModel();
        defaultTableModel.setRowCount(0);
        for (SanPham viewModelSanPham : list) {
            defaultTableModel.addRow(new Object[]{
                viewModelSanPham.getMaSach(), viewModelSanPham.getTenSach(), viewModelSanPham.getSoLuong(),
                viewModelSanPham.getGiaBan(),viewModelSanPham.getNgayThem(),
                viewModelSanPham.getDanhMuc(),viewModelSanPham.isTrangThai()
            }
            );
        }
    }

