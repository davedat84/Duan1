    public void loadTable(ArrayList<ViewModelMauSac> list) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblMauSacForm.getModel();
        defaultTableModel.setRowCount(0);
        for (ViewModelMauSac viewModelMauSac : list) {
            defaultTableModel.addRow(new Object[]{
                viewModelMauSac.getMa(), viewModelMauSac.getTen()
            });
        }
    }

