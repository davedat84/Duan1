    public SanPham getData() {
        SanPham sp = new SanPham();
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String soLuong = txtSluong.getText();
        String giaBan = txtGia.getText();
        String ngay = txtNgay.getText();
        
        sp.setMaSach(ma);
        sp.setTenSach(ten);
        sp.setSoLuong(soLuong);
        sp.setGiaBan(Double.parseDouble(giaBan));
        sp.setNgayThem(ngay);
        
        return sp;
    }


    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        SanPham sp = getData();
        JOptionPane.showMessageDialog(this, iSanPhamService.insert(sp));
        loadTable(iSanPhamService.getListSanPham());
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblSanPhamForm.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để xoá");
            return;
        }
        String ma = tblSanPhamForm.getValueAt(row, 0).toString();
        JOptionPane.showMessageDialog(this, iSanPhamService.deleteSanPhamAndChiTietSP(ma));
        loadTable(iSanPhamService.getListSanPham());
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblSanPhamFormMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamFormMouseClicked
        int row = tblSanPhamForm.getSelectedRow();
        String ma = tblSanPhamForm.getValueAt(row, 0).toString();
        String ten = tblSanPhamForm.getValueAt(row, 1).toString();
        txtMa.setText(ma);
        txtTen.setText(ten);
    }//GEN-LAST:event_tblSanPhamFormMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        SanPham sp = getData();

        int row = tblSanPhamForm.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng dể update");
            return;
        }
        String ma = tblSanPhamForm.getValueAt(row, 0).toString();
        JOptionPane.showMessageDialog(this, iSanPhamService.update(ma, sp));
        loadTable(iSanPhamService.getListSanPham());
    }//GEN-LAST:event_btnSuaActionPerformed

